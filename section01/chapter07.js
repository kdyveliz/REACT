// 1. 대입 연산자 (=)
let var1 = 1;

// 2. 산술 연산자 ( + , * , / , - , % )

// 3. 복합대입연산자
let num1 = 10;
num1 += 20

// 4. 증감연산자
let num2 = 10;
console.log(num2);
console.log(num2++); // 뒤 부터 적용 됨
console.log(++num2); // 바로 적용 됨 

// 5. 논리 연산자
let or = true || false;
let and = true && false;
let not = !true;

console.log(or,and,not);

// 6. 비교 연산자
// "===" 자료형까지 비교 "==" 자료형은 비교하지 않음
let comp1 = 1 === 2;
let comp2 = 1 !== 2;
console.log(comp1,comp2);

